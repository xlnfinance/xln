#!/usr/bin/env bun
/**
 * Inject git version info at build time
 * Auto-runs before dev/build to keep version fresh
 */

import { execFileSync } from 'node:child_process';
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname } from 'node:path';

const git = (...args: string[]): string =>
  execFileSync('git', args, { encoding: 'utf8', stdio: ['ignore', 'pipe', 'inherit'] }).trim();

const gitHash = git('rev-parse', 'HEAD');
const gitShort = git('rev-parse', '--short', 'HEAD');
const gitMessage = git('log', '-1', '--pretty=%B');
const version = {
  hash: gitHash,
  short: gitShort,
  message: gitMessage,
  date: git('log', '-1', '--format=%cd', '--date=iso'),
  branch: git('rev-parse', '--abbrev-ref', 'HEAD'),
  buildTime: new Date().toISOString(),
  githubUrl: `https://github.com/xlnfinance/xln/commit/${gitHash}`,
};

const outputPath = 'frontend/src/lib/generated/version.ts';
mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(
  outputPath,
  `// Auto-generated by scripts/inject-version.ts - DO NOT EDIT
export const VERSION = ${JSON.stringify(version, null, 2)} as const;
`,
);

console.log(`✅ Version info injected: ${gitShort} - ${gitMessage.split('\n')[0]!.slice(0, 50)}`);
